public class IntegerTask  {

    /**
     * Сумма чисел от 1 до n (1 + 2 + 3 + ... + n)
     * Пример: (5) -> 15
     */
    public static int sum(int n) {
        if(n==0) { return 0; }
        return sum(n-1)+n;
    }

    /**
     * Гусеница поднимается по стене длиной height на высоту top за день, ночью гусеница сползает на bottom.
     * Сколько дней понадобится гусенице, чтобы доползти до потолка. Если она этого никогда не сможет сделать,
     * Верните число Integer.MAX_VALUE;
     * Пример: (10, 3, 2) -> 8
     */
    public static int snake(int height, int top, int bottom) {
        if (top>=height) {return 1;}
        if (bottom>=top) { return Integer.MAX_VALUE; }
        int current_height=0, day=0;
        for(; ;){
            day++;
            current_height=current_height+top;
            if (current_height>=height) { return day; }
            current_height=current_height-bottom;
        }
    }

    /**
     * Дано число n и номер разряда order. Выведите цифру стояющую на нужном разряде
     * Пример: (454355, 3) -> 3
     */
    public static int kDecimal(int n, int order) {
        for(; ; ){ n=Math.abs(n);
            int answer;
            answer=n%10;
            n=n/10;
            order--;
            if (order==0) {
                return answer;
            }
        }
    }


    /**
     * Выведите факториал от числа n
     * Пример: (5) -> 120
     */
    public static long factorial(byte n) {
        if (n==0) return 1;
        return factorial((byte) (n-1))* n;
    }
}
